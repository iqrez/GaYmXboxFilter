#pragma once

#ifndef GAYM_REPORT_V1_H
#define GAYM_REPORT_V1_H

#include "protocol.h"

#define GAYM_REPORT_FLAG_NONE 0x00000000UL

#define GAYM_SEMANTIC_BUTTON_A          0x00000001UL
#define GAYM_SEMANTIC_BUTTON_B          0x00000002UL
#define GAYM_SEMANTIC_BUTTON_X          0x00000004UL
#define GAYM_SEMANTIC_BUTTON_Y          0x00000008UL
#define GAYM_SEMANTIC_BUTTON_LB         0x00000010UL
#define GAYM_SEMANTIC_BUTTON_RB         0x00000020UL
#define GAYM_SEMANTIC_BUTTON_BACK       0x00000040UL
#define GAYM_SEMANTIC_BUTTON_START      0x00000080UL
#define GAYM_SEMANTIC_BUTTON_LSTICK     0x00000100UL
#define GAYM_SEMANTIC_BUTTON_RSTICK     0x00000200UL
#define GAYM_SEMANTIC_BUTTON_GUIDE      0x00000400UL
#define GAYM_SEMANTIC_BUTTON_MISC       0x00000800UL
#define GAYM_SEMANTIC_BUTTON_DPAD_UP    0x00001000UL
#define GAYM_SEMANTIC_BUTTON_DPAD_RIGHT 0x00002000UL
#define GAYM_SEMANTIC_BUTTON_DPAD_DOWN  0x00004000UL
#define GAYM_SEMANTIC_BUTTON_DPAD_LEFT  0x00008000UL

#pragma pack(push, 1)
typedef struct _GAYM_REPORT_V1 {
    GAYM_PROTOCOL_HEADER Header;
    ULONGLONG Sequence;
    ULONGLONG TimestampQpc;
    ULONG Buttons;
    SHORT LeftStickX;
    SHORT LeftStickY;
    SHORT RightStickX;
    SHORT RightStickY;
    USHORT LeftTrigger;
    USHORT RightTrigger;
    ULONG ReportFlags;
} GAYM_REPORT_V1, *PGAYM_REPORT_V1;
#pragma pack(pop)

GAYM_STATIC_ASSERT(sizeof(GAYM_REPORT_V1) == 56, gaym_report_v1_must_be_56_bytes);

#endif
